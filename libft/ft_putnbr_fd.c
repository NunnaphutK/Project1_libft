#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	x;

	if (fd < 0)
		return ;
	if (n < 0 && n > -10)
		write(fd, "-", 1);
	else if (n > 9 || n < -9)
		ft_putnbr_fd(n / 10, fd);
	n = n % 10;
	if (n < 0)
		n = -n;
	x = n + '0';
	write(fd, &x, 1);
}
